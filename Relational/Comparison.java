package BasicsOfJava.Relational;

public class Comparison {

        public static void main() {
            int p=15, q=10;

            System.out.println(p==q ); // is equals to
            System.out.println(p!=q );// not equals to
            System.out.println(p<q );// smaller than
            System.out.println(p>q );//  greater than
            System.out.println(p<=q );// smaller than or equals to
            System.out.println(p>=q );// greater than or equals to

        }
    }
